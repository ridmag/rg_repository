package com.itelasoft.pso.web.nav;

import java.util.ArrayList;
import java.util.List;

public class NavBean {
	private List<NavBean> navBeans;
	private String title;
	
	public NavBean() {
		navBeans = new ArrayList<NavBean>();
		NavBean bean1 = new NavBean("Problems");
		NavBean bean2 = new NavBean("Alerts");
		
		navBeans.add(bean1);
		navBeans.add(bean2);
		
	}
	
	public NavBean(String title) {
		super();
		this.title = title;
	}

	public void setNavBeans(List<NavBean> navBeans) {
		this.navBeans = navBeans;
	}
	public List<NavBean> getNavBeans() {
		return navBeans;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getTitle() {
		return title;
	}
	
}
