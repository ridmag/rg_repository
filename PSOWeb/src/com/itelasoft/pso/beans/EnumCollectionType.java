package com.itelasoft.pso.beans;

public enum EnumCollectionType {
	
	ONETIME("onetime"){
		public String toString(){
			return "onetime";
		}
	},
	RECURSIVE("recursive"){
		public String toString(){
			return "recursive";
		}
	};
	private String id;
	private EnumCollectionType(String id){
		this.id = id;
	}
	public String getid(){
		return id;
	}

}
